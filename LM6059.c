void SendCmd(uchar Command)
{
	uchar j, bit7;
	L_CSreset;
	L_CKset;
	L_LDreset;
	for (j = 0; j < 8; j++)
	{
		bit7 = Command & 0x80;
		if (bit7 == 0)
		{
			L_DAreset;
		}
		else
		{
			L_DAset;
		}

		L_CKreset;
		L_CKset;
		Command = Command << 1;
	}
	L_CSset;
}

void SendData(uchar DDate)
{
	uchar j, bit7;
	L_CSreset;
	L_CKset;
	L_LDset;
	
	for (j = 0; j < 8; j++)
	{
		bit7 = DDate & 0x80;
		if (bit7 == 0)
		{
			L_DAreset;
		}
		else
		{
			L_DAset;
		}

		L_CKreset;
		L_CKset;
		DDate = DDate << 1;
	}
	L_CSset;
}

